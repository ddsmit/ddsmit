<h1>A Non-Coders Guide to the GitHUB</h1>
<h2>What is a GitHUB, the answer is not 42</h2>
<p>
    To start, we should talk about git. According to the <a href="https://git-scm.com/">Git website</a>, git is a 
    "free and open source distributed version control system". Lets break down the pieces of this description a bit.
</p>
<h3>Free</h3>
<p>
    This one has a couple different meanings. The first is users are not charged to use the software. The second is the software is
    licenses with the intent to keep the use of the code free to all users. The specifics can be found on the <a href="https://git-scm.com/">Git website</a>.
</p>
<h3></h3>Open Source - The code is available to the public, which allows contributions back to the code base from anyone.
